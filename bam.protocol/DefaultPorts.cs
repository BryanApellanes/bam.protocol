namespace Bam.Protocol;

/// <summary>
/// Defines the default port numbers used by the Bam protocol for different transports.
/// </summary>
public class DefaultPorts
{
    /// <summary>
    /// The default HTTP port (8080).
    /// </summary>
    public const int DefaultHttpPort = 8080;

    /// <summary>
    /// The default TCP port (8413).
    /// </summary>
    public const int DefaultTcpPort = 8413;

    /// <summary>
    /// The default UDP port (8414).
    /// </summary>
    public const int DefaultUdpPort = 8414;
}